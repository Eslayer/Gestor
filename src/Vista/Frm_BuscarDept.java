    private void jbtn_BuscarDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_BuscarDeptActionPerformed
String departamento;
int id;

RegistroDepto reg = new RegistroDepto();
DefaultTableModel modelo = (DefaultTableModel) this.jtbl_data.getModel();

try {
    id = Integer.parseInt(this.jtxt_buscarDept.getText());
} catch (NumberFormatException e) {
    id = 0; // Si no se ingresa un n√∫mero, buscar todos los departamentos.
}

modelo.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos.

if (id == 0) { // Listar todos los elementos
    ArrayList<Departamento> lista = reg.buscarTodosDept(); // Buscar todos los departamentos.
    for (Departamento dept : lista) {
        id = dept.getIdDept(); // Obtener el ID del departamento.
        departamento = dept.getNombreDept(); // Obtener el nombre del departamento.
        modelo.addRow(new Object[]{id, departamento}); // Agregar fila a la tabla.
    }
} else { // Buscar un departamento por ID
    Departamento dept = reg.buscarPorId(id); // Buscar el departamento por ID.
    if (dept != null) { // Si el departamento existe
        id = dept.getIdDept(); // Obtener el ID.
        departamento = dept.getNombreDept(); // Obtener el nombre del departamento.
        modelo.addRow(new Object[]{id, departamento}); // Agregar fila a la tabla.
    } else {
        // Si no se encuentra el departamento, agregar mensaje o manejar el caso.
        modelo.addRow(new Object[]{"No encontrado", "No encontrado"});
    }
}
    }//GEN-LAST:event_jbtn_BuscarDeptActionPerformed

